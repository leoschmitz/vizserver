VizCCOM::VizCCOM() : DllLoaded(false)
{
	LPVOID pv = NULL;
	if (CoInitialize(pv) == S_OK)
	{
		DllLoaded = LoadDll();
	}		
}
//-----------------------------------------------------------------------------
VizCCOM::~VizCCOM()
{
	CoUninitialize();
}
//-----------------------------------------------------------------------------
bool VizCCOM::LoadDll()
{
 	if (setControl("{A346E8F0-F890-432F-B8CD-6A5854030B48}"))
	{
		return true;
	}

	return false;
}
